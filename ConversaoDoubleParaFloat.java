public class ConversaoDoubleParaFloat {
    public static void main(String[] args) {
        double peso1 = 20.5;
        float peso2 = (float) peso1;

        float taxa1 = 934.5F;
        double taxa2 = taxa1;
        
        System.out.println(peso1);
        System.out.println(taxa2);
    }
}